// Translate the page content based on the selected language
function translatePage() {
    const lang = localStorage.getItem('siteLang') || 'ru';
    document.querySelectorAll("[data-translate]").forEach((el) => {
        const key = el.getAttribute("data-translate");
        if (translations[lang][key]) {
            // Preserve HTML content if present (e.g., spans or other tags)
            if (el.innerHTML.includes('<')) {
                const tempDiv = document.createElement('div');
                tempDiv.innerHTML = translations[lang][key];
                el.innerHTML = tempDiv.innerHTML;
            } else {
                el.textContent = translations[lang][key];
            }
        } else {
            console.warn(`Translation missing for key: ${key} in language: ${lang}`);
        }
    });
    // Update HTML lang attribute
    document.documentElement.setAttribute('lang', lang);
}

// Change language and update UI
function changeLanguage(lang) {
    localStorage.setItem('siteLang', lang);
    translatePage();
    // Update language selector
    const langSelect = document.querySelector('#lang');
    if (langSelect) {
        langSelect.value = lang;
    }
}

// Toggle navigation menu for mobile
function toggleMenu() {
    const menu = document.getElementById("nav-menu");
    menu.classList.toggle("show");
}

// Initialize language on page load
document.addEventListener("DOMContentLoaded", () => {
    const savedLang = localStorage.getItem('siteLang') || 'ru';
    changeLanguage(savedLang);
});